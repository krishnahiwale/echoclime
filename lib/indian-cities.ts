export function getAqiColor(aqi: number): string {
  if (aqi <= 50) return "text-emerald-500"
  if (aqi <= 100) return "text-green-500"
  if (aqi <= 150) return "text-yellow-500"
  if (aqi <= 200) return "text-orange-500"
  if (aqi <= 300) return "text-red-500"
  return "text-purple-500"
}

export function getAqiBackground(aqi: number): string {
  if (aqi <= 50) return "bg-emerald-500/10"
  if (aqi <= 100) return "bg-green-500/10"
  if (aqi <= 150) return "bg-yellow-500/10"
  if (aqi <= 200) return "bg-orange-500/10"
  if (aqi <= 300) return "bg-red-500/10"
  return "bg-purple-500/10"
}

export function getStatusBadgeColor(status: IndianCity["status"]): string {
  switch (status) {
    case "excellent":
      return "bg-emerald-500/10 text-emerald-500 border-emerald-500/20"
    case "good":
      return "bg-green-500/10 text-green-500 border-green-500/20"
    case "moderate":
      return "bg-yellow-500/10 text-yellow-500 border-yellow-500/20"
    case "poor":
      return "bg-orange-500/10 text-orange-500 border-orange-500/20"
    case "severe":
      return "bg-red-500/10 text-red-500 border-red-500/20"
  }
}
